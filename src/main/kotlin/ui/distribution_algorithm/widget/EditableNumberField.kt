package ui.distribution_algorithm.widget

import androidx.compose.foundation.layout.Box
import androidx.compose.foundation.layout.fillMaxWidth
import androidx.compose.foundation.layout.padding
import androidx.compose.foundation.text.BasicTextField
import androidx.compose.runtime.Composable
import androidx.compose.runtime.getValue
import androidx.compose.runtime.setValue
import androidx.compose.ui.Modifier
import androidx.compose.ui.text.TextStyle
import androidx.compose.ui.unit.dp
import androidx.compose.ui.unit.sp
import common.compose.BorderedTitledComposable
import common.theme.BlueMainLight

@Composable
fun EditableNumberField(
    modifier: Modifier = Modifier,
    title: String? = null,
    editableNumber: Int,
    maxNumberCount: Int,
    onDataChanged: (String) -> Unit,
) {
    val stringPool = "01234567890"

    BorderedTitledComposable(
        modifier = modifier,
        title = title ?: "",
    ) {
        BasicTextField(
            value = if (editableNumber == 0) "" else editableNumber.toString(),
            onValueChange = {
                if (it.isEmpty() || it.length in (1..maxNumberCount) && it.last() in stringPool) {
                    onDataChanged(it)
                }
            },
            textStyle =
                TextStyle(
                    fontSize = 18.sp,
                    color = BlueMainLight,
                ),
            modifier = Modifier.fillMaxWidth(),
            decorationBox = { innerTextField ->
                Box(modifier = Modifier.padding(12.dp)) {
                    innerTextField()
                }
            },
        )
    }
}
